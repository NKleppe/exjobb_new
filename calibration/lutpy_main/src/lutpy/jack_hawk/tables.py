def get_summed_value_from_table_by_2_indices(z_val: float, z_ref: list[float], hv_val: float, hv_ref: list[float], table: list[list[list[float]]]) -> float:
    z_ref_index = z_ref.index(z_val)
    hv_ref_index = hv_ref.index(hv_val)

    return table[z_ref_index][hv_ref_index][0] + table[z_ref_index][hv_ref_index][1]

