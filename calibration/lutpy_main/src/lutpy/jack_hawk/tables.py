def McEllan_Vk_interpolation(Z, k):
    # this function takes Z (ranging from 6 to 92) and k (ranging from 1 to 4),
    # and returns the value Vk after interpolating in table 2 from Mcellan et al.
    # (Physical Review A, 1976)

    Z_ref = (6, 8, 10, 12, 13, 16, 18, 20, 26, 36, 50, 54, 74, 79, 86, 92)
    V = ([-0.7866, 0.5034, -0.2935, 0.1543],
         [-0.8689, 0.6041, -0.4439, 0.3280],
         [-0.9417, 0.6533, -0.5186, 0.4735],
         [-0.9709, 0.6535, -0.3359, 0.1371],
         [-0.9734, 0.6559, -0.3260, 0.1256],
         [-1.016, 0.7842, -0.4659, 0.2164],
         [-1.047, 0.8842, -0.6012, 0.3239],
         [-1.034, 0.8476, -0.5341, 0.2612],
         [-1.072, 0.9381, -0.6733, 0.3848],
         [-1.074, 0.8285, -0.4875, 0.2237],
         [-1.108, 0.9368, -0.6065, 0.3046],
         [-1.109, 0.9453, -0.6141, 0.3084],
         [-1.108, 0.8934, -0.5477, 0.2606],
         [-1.124, 0.9377, -0.6024, 0.3014],
         [-1.105, 0.8731, -0.5123, 0.2311],
         [-1.100, 0.8612, -0.4949, 0.2177])

    # Check which entry in Z_ref will be the upper limit of the interpolation
    i = 0
    while Z_ref[i] < Z:
        i = i + 1

    # If Z=Z_ref(i) then return Vk(Z_ref(i)) from the table.
    # If not, interpolate between the upper limit and Z_ref(i-1)
    # and return Vk(Z)

    if Z_ref[i] == Z:
        Vk = V[i][k]
        return Vk

    dZ = (Z_ref[i] - Z_ref[i - 1])
    dV = (V[i][k]) - V[i - 1][k]

    Vk = V[i - 1][k] + (Z - Z_ref[i - 1]) * dV / dZ
    return Vk
